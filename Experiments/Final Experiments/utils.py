def plot_model(vae, rng_key, X_test, share_axis=True, pad=0.5):
    """
    A unified plotting function for standard, Global, and Hierarchical VAEs.
    Dynamically identifies if the bottleneck is 'z' or 'z0', and if 'm' exists.
    """
    rng_key, e_sub, d_sub, s_sub = jax.random.split(rng_key, 4)

    # 1. Encode Data
    values = vae.encode_batch(X_test, e_sub)
    
    # Identify the main latent keys
    z_key = "z0" if "z0" in values else "z"
    has_m = "m" in values
    c_recon = values.get("pidx")

    # 2. Reconstruct Data
    latent_inputs = {key: val for key, val in values.items() if key.startswith("z") or key == "m"}
    X_recon = vae.decode_latent(latent_inputs, d_sub, size_site=z_key)
    
    plot3d([(X_recon["obs"], "recon")], title="3D Reconstruction")

    # 3. Setup 2x2 Subplots for Latents
    _, axes = plt.subplots(2, 2, figsize=(12, 10), sharex=share_axis, sharey=share_axis)
    for ax in axes.flat:
        ax.set_aspect("equal")

    # Calculate percentiles for dynamic zooming (ignores outliers)
    z_data = np.asarray(values[z_key])
    xlim = (np.percentile(z_data[:, 0], 2) - pad, np.percentile(z_data[:, 0], 98) + pad)
    ylim = (np.percentile(z_data[:, 1], 2) - pad, np.percentile(z_data[:, 1], 98) + pad)

    # Top-Left: Reconstructed z/z0
    axes[0, 0].scatter(z_data[:, 0], z_data[:, 1], c=c_recon, s=12)
    axes[0, 0].set_title(f"Reconstructed Latent ({z_key})")
    axes[0, 0].set_xlim(xlim)
    axes[0, 0].set_ylim(ylim)

    # Top-Right: Reconstructed m (if exists)
    if has_m:
        m_data = np.asarray(values["m"])
        axes[0, 1].scatter(m_data[:, 0], m_data[:, 1], c=c_recon, s=12)
        axes[0, 1].set_title("Reconstructed Latent (m)")
        axes[0, 1].set_xlim(np.percentile(m_data[:, 0], 2) - pad, np.percentile(m_data[:, 0], 98) + pad)
        axes[0, 1].set_ylim(np.percentile(m_data[:, 1], 2) - pad, np.percentile(m_data[:, 1], 98) + pad)
    else:
        axes[0, 1].axis("off")
        axes[0, 1].set_title("No global 'm' in model")

    # 4. Generate New Samples
    values_sample = vae.sample(s_sub, 1000)
    plot3d([(values_sample["obs"], "Samples"), (X_test, "Ground Truth")], title="3D Samples vs Ground Truth")

    c_samp = values_sample.get("pidx")
    z_data_s = np.asarray(values_sample[z_key])

    # Bottom-Left: Sampled z/z0
    axes[1, 0].scatter(z_data_s[:, 0], z_data_s[:, 1], c=c_samp, s=12)
    axes[1, 0].set_title(f"Sampled Latent ({z_key})")
    axes[1, 0].set_xlim(xlim)
    axes[1, 0].set_ylim(ylim)

    # Bottom-Right: Sampled m (if exists)
    if has_m:
        m_s = np.asarray(values_sample["m"])
        axes[1, 1].scatter(m_s[:, 0], m_s[:, 1], c=c_samp, s=12)
        axes[1, 1].set_title("Sampled Latent (m)")
        axes[1, 1].set_xlim(np.percentile(m_s[:, 0], 2) - pad, np.percentile(m_s[:, 0], 98) + pad)
        axes[1, 1].set_ylim(np.percentile(m_s[:, 1], 2) - pad, np.percentile(m_s[:, 1], 98) + pad)
    else:
        axes[1, 1].axis("off")
        axes[1, 1].set_title("No global 'm' in model")

    plt.tight_layout()
    plt.show()




def plot_stein_norms(per_particle_norms, title, iteration_slice=slice(None)):
    norms = np.asarray(jax.device_get(per_particle_norms))

    # norms shape: [iterations, particles]
    norms = norms[iteration_slice]

   

    iterations = np.arange(norms.shape[0])

    if iteration_slice.start is not None:
        iterations = iterations + iteration_slice.start

    mean_norm = norms.mean(axis=1)
    std_norm = norms.std(axis=1)
    max_norm = norms.max(axis=1)

    eps = 1e-12
    norms = np.maximum(norms, eps)
    mean_norm = np.maximum(mean_norm, eps)
    std_norm = np.maximum(std_norm, eps)
    max_norm = np.maximum(max_norm, eps)

    plt.figure(figsize=(14, 5))


    plt.semilogy(iterations, mean_norm, linewidth=2, label="mean")
    plt.semilogy(iterations, max_norm, linewidth=2, linestyle="--", label="max")
    plt.fill_between(iterations, mean_norm - std_norm, mean_norm + std_norm, color='blue', alpha=0.2, label="std")

    plt.xlabel("Iteration")
    plt.ylabel("Stein particle norm")
    plt.title(title)
    plt.legend()
    fig = plt.gcf()
    plt.show()
    return fig



def plot_individual_norm_paths(per_particle_norms, title, iteration_slice=slice(None), window=3):

    norms = np.asarray(jax.device_get(per_particle_norms))

    # norms shape: [iterations, particles]
    norms = norms[iteration_slice]

    # add smoothing with a lag of 3 for each path
    norms =np.apply_along_axis(lambda m: np.convolve(m, np.ones(window)/window, mode='same'), axis=0, arr=norms)




    iterations = np.arange(norms.shape[0])

    if iteration_slice.start is not None:
        iterations = iterations + iteration_slice.start

    plt.figure(figsize=(14, 5))

    for i in range(norms.shape[1]):
        plt.semilogy(iterations, norms[:, i], linewidth=1, label=f"particle {i}")

    plt.xlabel("Iteration")
    plt.ylabel("Stein particle norm")
    plt.title(title)
    plt.legend()
    fig = plt.gcf()
    plt.show()

    # return image:
    return fig



