class FIDCalculator:
    def __init__(self):
        self.device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
        self.fid = FrechetInceptionDistance(feature=64, normalize=True).to(self.device)
    
    def color3(self, images):
        return images.reshape(-1, 1, 28, 28).expand(-1, 3, -1, -1)
    
    def calculate_fid(self, real_data, sampled_data, batch_size = 128):
        
        self.fid.reset()
        for i in range(0, len(real_data), batch_size):
            x = real_data[i:i+batch_size].to(self.device)
            self.fid.update(self.color3(x), real=True)
        
        for i in range(0, len(sampled_data), batch_size):
            x = sampled_data[i:i+batch_size].to(self.device)
            self.fid.update(self.color3(x), real=False)
        
        return self.fid.compute().cpu().item()
    

