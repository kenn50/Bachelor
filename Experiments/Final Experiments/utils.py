def plot_linear_interpolation(vae, rng_key, data1, data2, num_steps=10, ax_list=None):
    """
    Linearly interpolates in the latent space of any VAE (Ordinary, Global, or Hierarchical).
    Dynamically infers the latent nodes (z, z0, m, etc.) and evaluates interpolation.
    """
    # Reshape boundary points for batch_size=1
    d1 = jnp.expand_dims(data1, axis=0)
    d2 = jnp.expand_dims(data2, axis=0)
    
    encoded_1 = vae.encode_batch(d1, rng_key)
    encoded_2 = vae.encode_batch(d2, rng_key)
    
    # Identify the main latent keys relevant for reproduction
    relevant_keys = [k for k in encoded_1.keys() if k.startswith('z') or k == 'm']
    z_key = "z0" if "z0" in encoded_1 else "z"
    
    alphas = np.linspace(0, 1, num_steps)
    interpolated_images = []
    
    for alpha in alphas:
        interp_latents = {}
        for key in relevant_keys:
            val1 = encoded_1[key]
            val2 = encoded_2[key]
            interp_latents[key] = (1.0 - alpha) * val1 + alpha * val2
            
        recon = vae.decode_latent(interp_latents, rng_key, size_site=z_key)
        
        img = jax.nn.sigmoid(recon["x"])
        interpolated_images.append(np.array(img).squeeze())
        
    created_fig = False
    if ax_list is None:
        fig, ax_list = plt.subplots(1, num_steps, figsize=(num_steps * 1.5, 2))
        created_fig = True
        
    for i, ax in enumerate(ax_list):
        ax.imshow(interpolated_images[i].reshape(28, 28), cmap="gray")
        ax.axis("off")
        ax.set_title(f"a={alphas[i]:.1f}")
        
    if created_fig:
        plt.tight_layout()
        plt.show()    






