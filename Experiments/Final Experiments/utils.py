def compute_js_divergence(phi1, psi1, phi2, psi2, bins=36, epsilon=1e-8):
    """
    Computes the Jensen-Shannon Divergence between two sets of phi/psi angles.
    bins=36 means 10-degree by 10-degree bins across the 360-degree space (-180 to 180).
    """
    # 1. Define bin edges from -180 to 180
    edges = np.linspace(-180, 180, bins + 1)
    
    # 2. Create 2D histograms (Binning)
    H1, _, _ = np.histogram2d(phi1, psi1, bins=[edges, edges])
    H2, _, _ = np.histogram2d(phi2, psi2, bins=[edges, edges])
    
    # Flatten into 1D arrays for discrete probability distributions
    p = H1.flatten()
    q = H2.flatten()
    
    # 3. Add pseudocount to avoid zero-bin issues
    p = p + epsilon
    q = q + epsilon
    
    # 4. Normalize so they sum to 1
    p = p / np.sum(p)
    q = q / np.sum(q)
    
    # Compute Jensen-Shannon Divergence 
    # (scipy function returns JS Distance, which is the square root of JS Divergence)
    js_dist = jensenshannon(p, q)
    js_divergence = js_dist ** 2
    
    return js_divergence