def plot3d(d, title="3D plot"):
    """
    d: list of (dataset, label)
       Each dataset should be array-like with shape (n, 3) or reshapeable to (-1, 3).
    """

    fig = go.Figure()

    for dataset, label in d:
        points = np.asarray(dataset).reshape(-1, 3)

        fig.add_trace(
            go.Scatter3d(
                x=points[:, 0],
                y=points[:, 1],
                z=points[:, 2],
                mode="markers",
                name=label,
                marker=dict(size=2),
            )
        )

    fig.update_layout(
        title=dict(
            text=title,
            x=0.5,
            y=0.95,
            font=dict(
                family="Arial",
                size=24,
                color="navy",
            ),
        ),
        margin=dict(t=60),
        scene=dict(
            xaxis_title="X",
            yaxis_title="Y",
            zaxis_title="Z",
        ),
    )

    fig.show()





