def extract_angles(pdb_path):
    """Standalone angle extractor for the JSD calculation."""
    parser = PDBParser(QUIET=True)
    structure = parser.get_structure("ensemble", pdb_path)
    phi_list, psi_list = [], []
    for model in structure:
        for chain in model:
            polypeptides = PPBuilder().build_peptides(chain)
            for poly in polypeptides:
                for phi, psi in poly.get_phi_psi_list():
                    if phi and psi:
                        phi_list.append(math.degrees(phi))
                        psi_list.append(math.degrees(psi))
