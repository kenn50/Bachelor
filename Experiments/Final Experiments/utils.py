def plot_compare_umap_kde(reconstructed, samples, title):
    reducer = umap.UMAP()
    both_emb = reducer.fit_transform(np.vstack([reconstructed, samples]))
    
    recon_emb = both_emb[:len(reconstructed)]
    samples_emb = both_emb[len(reconstructed):]
    # create a figure ax with two subplots
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(20, 8))

    #kde of reconstructions
    kde = gaussian_kde(recon_emb.T)
    x_min, x_max = recon_emb[:, 0].min() - 1, recon_emb[:, 0].max() + 1
    y_min, y_max = recon_emb[:, 1].min() - 1, recon_emb[:, 1].max() + 1
    xx, yy = np.mgrid[x_min:x_max:100j, y_min:y_max:100j]
    grid_coords = np.vstack([xx.ravel(), yy.ravel()])
    density = kde(grid_coords).reshape(xx.shape)
    ax1.contourf(xx, yy, density, levels=50, cmap="viridis")
    ax1.contour(xx, yy, density, levels=10, colors="black", linewidths=0.5)
    ax1.set_title("KDE of reconstructions")

    #kde of samples:
    kde_samples = gaussian_kde(samples_emb.T)
    x_min_s, x_max_s = samples_emb[:, 0].min() - 1, samples_emb[:, 0].max() + 1
    y_min_s, y_max_s = samples_emb[:, 1].min() - 1, samples_emb[:, 1].max() + 1
    xx_s, yy_s = np.mgrid[x_min_s:x_max_s:100j, y_min_s:y_max_s:100j]
    grid_coords_s = np.vstack([xx_s.ravel(), yy_s.ravel()])
    density_s = kde_samples(grid_coords_s).reshape(xx_s.shape)
    ax2.contourf(xx_s, yy_s, density_s, levels=50, cmap="viridis")
    ax2.contour(xx_s, yy_s, density_s, levels=10, colors="black", linewidths=0.5)
    ax2.set_title("KDE of samples")


    #share axes
    x_min = min(x_min, x_min_s)
    x_max = max(x_max, x_max_s)
    y_min = min(y_min, y_min_s)
    y_max = max(y_max, y_max_s)
    ax1.set_xlim(x_min, x_max)
    ax1.set_ylim(y_min, y_max)
    ax2.set_xlim(x_min, x_max)
    ax2.set_ylim(y_min, y_max)  

    plt.close()

    return fig






